export class AssetLoader {
    constructor() {
        this.images = {};
        this.loaded = 0;
        this.total = Object.keys(ASSET_MANIFEST).length;
    }

    load(onProgress) {
        return new Promise((resolve) => {
            const entries = Object.entries(ASSET_MANIFEST);
            let done = 0;

            entries.forEach(([key, path]) => {
                const img = new Image();
                img.onload = () => {
                    this.images[key] = img;
                    done++;
                    if (onProgress) onProgress(done / entries.length);
                    if (done === entries.length) resolve(this.images);
                };
                img.onerror = () => {
                    this.images[key] = null;
                    done++;
                    if (onProgress) onProgress(done / entries.length);
                    if (done === entries.length) resolve(this.images);
                };
                img.src = path;
            });
        });
    }

    get(key) {
        return this.images[key];
    }
}
